  do i = 1, 10
    r = r8_random ( s1, s2, s3 )
    write ( *, '(2x,g14.6,2x,i8,2x,i8,2x,i8)' ) r, s1, s2, s3
  end do

  return
end
subroutine test08 ( )

!*****************************************************************************80
!
!! TEST08 tests R8_RANDOM.
!
!  Licensing:
!
!    This code is distributed under the MIT license.
!
!  Modified:
!
!    08 July 2008
!
!  Author:
!
!    John Burkardt
!
  implicit none

  integer, parameter :: rk = kind ( 1.0D+00 )

  integer n
  parameter ( n = 100000 )

  integer i
  real ( kind = rk ) r8_random
  integer s1
  integer s2
  integer s3
  real ( kind = rk ) u(n)
  real ( kind = rk ) u_avg
  real ( kind = rk ) u_var

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'TEST08'
  write ( *, '(a)' ) '  Examine the average and variance of a'
  write ( *, '(a)' ) '  sequence generated by R8_RANDOM.'
!
!  Start with known seeds.
!
  s1 = 12345
  s2 = 34567
  s3 = 56789

  write ( *, '(a)' ) ' '
  write ( *, '(a,i10,a)' ) '  Now compute ', n, ' elements.'

  u_avg = 0.0D+00
  do i = 1, n
    u(i) = r8_random ( s1, s2, s3 )
    u_avg = u_avg + u(i)
  end do

  u_avg = u_avg / real ( n, kind = rk )

  u_var = 0.0D+00
  do i = 1, n
    u_var = u_var + ( u(i) - u_avg ) * ( u(i) - u_avg )
  end do

  u_var = u_var / real ( n - 1, kind = rk )

  write ( *, '(a)' ) ' '
  write ( *, '(a,f10.6)' ) '  Average value = ', u_avg
  write ( *, '(a,f10.6)' ) '  Expecting       ', 0.5D+00

  write ( *, '(a)' ) ' '
  write ( *, '(a,f10.6)' ) '  Variance =      ', u_var
  write ( *, '(a,f10.6)' ) '  Expecting       ', 1.0D+00 / 12.0D+00

  return
end
subroutine test09 ( )

!*****************************************************************************80
!
!! TEST09 tests R8_RANDOM.
!
!  Licensing:
!
!    This code is distributed under the MIT license.
!
!  Modified:
!
!    08 July 2008
!
!  Author:
!
!    John Burkardt
!
  implicit none

  integer, parameter :: rk = kind ( 1.0D+00 )

  integer i
  real ( kind = rk ) r
  real ( kind = rk ) r8_random
  integer s1
  integer s1_save
  integer s2
  integer s2_save
  integer s3
  integer s3_save

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'TEST09'
  write ( *, '(a)' ) '  Show how the seeds used by R8_RANDOM,'
  write ( *, '(a)' ) '  which change on each step, can be reset to'
  write ( *, '(a)' ) '  restore any part of the sequence.'

  s1_save = 12345
  s2_save = 34567
  s3_save = 56789

  s1 = s1_save
  s2 = s2_save
  s3 = s3_save

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  Begin sequence with following seeds:'
  write ( *, '(a)' ) ' '
  write ( *, '(a,i8)' ) '  S1 = ', s1
  write ( *, '(a,i8)' ) '  S2 = ', s2
  write ( *, '(a,i8)' ) '  S3 = ', s3
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '         I      R                 S1        S2        S3'
  write ( *, '(a)' ) ' '

  do i = 1, 10

    r = r8_random ( s1, s2, s3 )
    write ( *, '(2x,i8,2x,g14.6,2x,i8,2x,i8,2x,i8)' ) i, r, s1, s2, s3

    if ( i == 5 ) then
      s1_save = s1
      s2_save = s2
      s3_save = s3
    end if

  end do

  s1 = s1_save
  s2 = s2_save
  s3 = s3_save

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  Restart the sequence, using the seeds'
  write ( *, '(a)' ) '  produced after step 5:'
  write ( *, '(a)' ) ' '
  write ( *, '(a,i8)' ) '  S1 = ', s1
  write ( *, '(a,i8)' ) '  S2 = ', s2
  write ( *, '(a,i8)' ) '  S3 = ', s3
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '         I      R                 S1        S2        S3'
  write ( *, '(a)' ) ' '

  do i = 1, 10

    r = r8_random ( s1, s2, s3 )
    write ( *, '(2x,i8,2x,g14.6,2x,i8,2x,i8,2x,i8)' ) i, r, s1, s2, s3

  end do

  return
end
