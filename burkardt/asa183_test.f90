  write ( *, '(a)' ) '  R8_RANDOM computes pseudorandom values.'
  write ( *, '(a)' ) '  Three seeds, S1, S2, and S3, are used.'

  s1 = 12345
  s2 = 34567
  s3 = 56789

  write ( *, '(a)' ) ' '
